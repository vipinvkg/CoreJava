package com.raystech.basics;

public class IfelseMulti {

	public static void main(String[] args) {
		
		int a = 55;
		
		if (a>100) {
			System.out.println("you can buy large");
			
		}else if(a > 70){
			System.out.println("you can buy medium");
			
		}else if(a > 50) {
			System.out.println("you can buy small");
			
		}else{
			System.out.println("sorry you can't");
		}
			
		}
		
	
}
