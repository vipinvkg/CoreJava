package com.raystech.basics;

public class Math_lang {

	public static void main(String[] args) {
		
		System.out.println(Math.max(5, 10));
		System.out.println(Math.min(10, 7));
		System.out.println(Math.addExact(10, 5));
		System.out.println(Math.subtractExact(10, 5));
		System.out.println(Math.decrementExact(5));
		System.out.println(Math.incrementExact(10));
		System.out.println(Math.multiplyExact(10, 5));
		System.out.println(Math.PI);
		System.out.println(Math.round(1.5));
		System.out.println(Math.sqrt(4));
	}
}
