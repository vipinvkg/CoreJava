package Core_exersice;

public class Switchcase_string {

	public static void main(String[] args) {

	
		switch ("java") {

		case "java":
			System.out.println("Vipin");
			break;

		case "python":
			System.out.println("Gupta");
			break;
		case "c++":
			System.out.println("Kumar");
			break;
		case "c":
			System.out.println("Omji");
			break;
		case "html":
			System.out.println("Sharma");
			break;

		default:
			System.out.println("Shiv");
		}
	}
	}
